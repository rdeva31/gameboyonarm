	orr r0, r0, \r, lsl #24
.endm

.macro SET_C r
	bic r0, r0, r11, lsl #16
	orr r0, r0, \r, lsl #16
.endm

.macro SET_F r
	bic r0, r0, r11
	orr r0, r0, \r
.endm

.macro SET_FLAG_Z
	orr r0, r0, #0x80
.endm

.macro SET_FLAG_N
	orr r0, r0, #0x40
.endm

