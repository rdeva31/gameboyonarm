.macro UPDATE_FLAG_Z
	orrne r0, r0, #0x80
	biceq r0, r0, #0x80
.endm

.macro UPDATE_FLAG_H r, m
	tst \r, \m
	orrne r0, r0, #0x20
	biceq r0, r0, #0x20
.endm

.macro UPDATE_FLAG_C r, m
	tst \r, \m
	orrne r0, r0, #0x10
	biceq r0, r0, #0x10
.endm

