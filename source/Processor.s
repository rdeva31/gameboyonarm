	tst \r, \m
	orreq r0, r0, #0x20
	bicne r0, r0, #0x20
.endm

.macro UPDATE_FLAG_C_NEG r, m
	tst \r, \m
	orreq r0, r0, #0x10
	bicne r0, r0, #0x10
